<h3>Stammblätter</h3>
<p>
	Jeder Mitarbeiter hat sein eigenes Zeiten-Stammblatt, dabei werden hier allerdings nur die "offiziellen" Zeiten wie:
	<ul>
		<li>Urlaub</li>
		<li>Krankheit</li>
		<li>Umzug</li>
		<li>Hochzeit</li>
	</ul>
	aufgeführt.
</p>
<p>
	Als Einstiegsseite für die Pflege dient eine tabellarische Liste aller Mitarbeiter, mit der angebundenen Aktion: <br>
	<b>"anzeigen" bei "anklicken der Reihe"</b>
</p>
<p>
	Danach öffnet sich das Urlaubsstammblatt des Mitarbeiters, welches in die folgenden Sektionen aufgeteilt ist:
</p>
<h3>Mitarbeiter</h3>
<p>
	Hier steht:
	<ul>
		<li>Mitarbeitername</li>
		<li>Region</li>
		<li>Kostenstelle</li>
		<li>und sein Eintrittsdatum</li>
	</ul>
	Das Eintrittsdatum wird in AT dazu benötigt um den Zeitpunkt des entstehens des Urlaubsanspruches zu identifizieren.
</p>
<h3>Statistiken/Zeitplan</h3>
<h4>Statistiken</h4>
<p>
	Innerhalb der Statistiken kann der Sachbearbeiter erkennen, wie der aktuelle Urlaubsanspruch des Mitarbeiters ist, wieviel 
	Verbrauch im aktuellen Betrachtungszeitraum vorliegt und wieviel sonstige Zeiten nach Kategorie gebucht wurden.
	Achtung: Keine "normalen" Zeiterfassungseinträge.
</p>
<h4>Zeitplan</h4>
<p>
	Durch anklicken des Reiter "Zeitplan" gelangt man zum aktuellen Arbeitszeitmodell des Mitarbeiters. Sollte noch keiner
	definiert worden sein, so weisst das Programm auch auf diesen Misstand hin! Siehe rote Infobox am Kopf des Formulars.
</p>
