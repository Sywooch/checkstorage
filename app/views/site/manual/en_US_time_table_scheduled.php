<h3>Worktime</h3>
<p>
	Each Location has it's on time schedule for it's employees. And each employee has a seperate plan
	for even or odd weeks in the year. Taking care about this situation, the personal management
	has to define a set of rules based upon the employee (user), the location and the odd and even weeks.
</p>
