<h3>Blog - Nachrichten</h3>
<p>
	Im Blog werden sämtliche Nachrichten/Artikel eingepflegt, die für die Mitarbeiter als zeitlich befristete Information
	zur Verfügung stehen sollen. Diese werden druch die Vergabe von "Schlagwörtern (Tags)" für die jeweilige Empfängergruppe
	vorsortiert. Erlaubte Schlagwörter sind (generell alles, folgende mit Bedeutung):
	<ul>
		<li>All - Für alle Mitarbeiter interessante Nachrichten.</li>
		<li>Region - (Berlin, Wien, Zürich) - für die genannte Region betreffende Nachrichten.</li>
		<li>Standort - (AT010) - für die den Standort betreffenden Nachrichten.</li>
		<li>Organisation - (Operations, Marketing, IT) - für die Orga Einheit</li>
	</ul>
	Bitte beachten Sie, dass mehrfach Verschlagwortung funktioniert und gewünscht ist.
</p>
